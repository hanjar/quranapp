export const TOTAL_PAGES = PAGE_MAP.length - 1; // 604

interface PageSection {
  surah: number;
  startAyah: number;
  endAyah: number;
}

interface PageInfo {
  page: number;
  sections: PageSection[];
}

/**
 * Get metadata about which surahs/ayahs appear on a given mushaf page.
 */
export function getPageInfo(pageNum: number): PageInfo | null {
  if (pageNum < 1 || pageNum > TOTAL_PAGES) return null;

  const [startSurah, startAyah] = PAGE_MAP[pageNum];

  // Determine end boundary
  let endSurah: number;
  let endAyah: number;
  if (pageNum < TOTAL_PAGES) {
    [endSurah, endAyah] = PAGE_MAP[pageNum + 1];
  } else {
    // Last page goes to end of Quran (surah 114, ayah 6)
    endSurah = 115; // sentinel
    endAyah = 1;
  }

  const sections: PageSection[] = [];

  if (startSurah === endSurah || (pageNum === TOTAL_PAGES)) {
    // Single surah on this page (or last page)
    if (pageNum === TOTAL_PAGES) {
      // Last page: from startSurah:startAyah to end of surah 114
      let currentSurah = startSurah;
      while (currentSurah <= 114) {
        const meta = SURAH_LIST.find(s => s.number === currentSurah);
        if (!meta) break;
        const from = currentSurah === startSurah ? startAyah : 1;
        sections.push({
          surah: currentSurah,
          startAyah: from,
          endAyah: meta.totalVerses,
        });
        currentSurah++;
      }
    } else {
      // Same surah: from startAyah to endAyah - 1
      const meta = SURAH_LIST.find(s => s.number === startSurah);
      sections.push({
        surah: startSurah,
        startAyah,
        endAyah: endAyah - 1,
      });
    }
  } else {
    // Cross-surah page: spans from startSurah to endSurah
    let currentSurah = startSurah;
    while (currentSurah <= Math.min(endSurah, 114)) {
      const meta = SURAH_LIST.find(s => s.number === currentSurah);
      if (!meta) break;

      let from: number;
      let to: number;

      if (currentSurah === startSurah) {
        from = startAyah;
        to = meta.totalVerses;
      } else if (currentSurah === endSurah) {
        from = 1;
        to = endAyah - 1;
      } else {
        from = 1;
        to = meta.totalVerses;
      }

      // Only add section if it has valid ayahs
      if (to >= from) {
        sections.push({ surah: currentSurah, startAyah: from, endAyah: to });
      }
      currentSurah++;
    }
  }

  return { page: pageNum, sections };
}

/**
 * Find the mushaf page number for a given surah and ayah.
 */
export function findPage(surah: number, ayah: number): number {
  for (let i = TOTAL_PAGES; i >= 1; i--) {
    const [s, a] = PAGE_MAP[i];
    if (surah > s || (surah === s && ayah >= a)) {
      return i;
    }
  }
  return 1;
}

/**
 * Get the first mushaf page of a given surah.
 */
export function getSurahFirstPage(surahNum: number): number {
  return findPage(surahNum, 1);
}
